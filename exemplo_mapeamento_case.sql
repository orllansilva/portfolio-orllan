/* Exemplo resumido de mapeamento CASE baseado em UD2 e UD3
   (usado em integrações Oracle EPM / FDMEE / Data Management) */

CASE
    -- UD2 = 0301A
    WHEN UD2 = '0301A' AND UD3 = '19001004' THEN '0301A-19001004'
    WHEN UD2 = '0301A' AND UD3 = '21800000' THEN '0301A-21800000'
    WHEN UD2 = '0301A' AND UD3 = '10050000' THEN '0301A-10050000'

    -- UD2 = 0309J
    WHEN UD2 = '0309J' AND UD3 = '12901001' THEN '0309J-12901001'
    WHEN UD2 = '0309J' AND UD3 = '10050000' THEN '0309J-10050000'
    WHEN UD2 = '0309J' AND UD3 = '10051000.1929' THEN '0309J-10051000.1929'

    -- UD2 = 0401A
    WHEN UD2 = '0401A' AND UD3 = '12901001' THEN '0401A-12901001'
    WHEN UD2 = '0401A' AND UD3 = '10050000' THEN '0401A-10050000'
    WHEN UD2 = '0401A' AND UD3 = '10052008' THEN '0401A-10052008'

    ELSE 'SEM_MAPEAMENTO'
END AS MAPEAMENTO
